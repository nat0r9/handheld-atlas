-- HandheldAtlas Phase 6
-- Game ratings, anti-abuse guardrails and monthly Community Top 5.

create table if not exists public.game_ratings (
  game_id uuid not null references public.games(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  rating smallint not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (game_id, user_id)
);

alter table public.game_ratings
add column if not exists created_at timestamptz not null default now();

alter table public.game_ratings
add column if not exists updated_at timestamptz not null default now();

alter table public.game_ratings
drop constraint if exists game_ratings_rating_check;

alter table public.game_ratings
add constraint game_ratings_rating_check
check (rating between 1 and 5);

create unique index if not exists game_ratings_game_user_uidx
on public.game_ratings(game_id, user_id);

create index if not exists game_ratings_game_idx
on public.game_ratings(game_id);

create index if not exists game_ratings_updated_idx
on public.game_ratings(updated_at desc);

create table if not exists public.game_rating_monthly (
  game_id uuid not null references public.games(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  rating_month date not null,
  rating smallint not null check (rating between 1 and 5),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (game_id, user_id, rating_month),
  constraint game_rating_monthly_month_start_check
  check (rating_month = date_trunc('month', rating_month::timestamp)::date)
);

create index if not exists game_rating_monthly_month_game_idx
on public.game_rating_monthly(rating_month, game_id);

create table if not exists public.game_rating_events (
  id bigint generated by default as identity primary key,
  game_id uuid not null references public.games(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  action text not null check (action in ('set', 'remove')),
  rating smallint null check (rating is null or rating between 1 and 5),
  created_at timestamptz not null default now()
);

create index if not exists game_rating_events_user_time_idx
on public.game_rating_events(user_id, created_at desc);

create index if not exists game_rating_events_game_time_idx
on public.game_rating_events(game_id, created_at desc);

-- Preserve ratings already cast during the current UTC month.
insert into public.game_rating_monthly (
  game_id,
  user_id,
  rating_month,
  rating,
  created_at,
  updated_at
)
select
  game_id,
  user_id,
  date_trunc('month', updated_at at time zone 'utc')::date,
  rating,
  created_at,
  updated_at
from public.game_ratings
where updated_at >= date_trunc('month', now() at time zone 'utc')
on conflict (game_id, user_id, rating_month)
do update set
  rating = excluded.rating,
  updated_at = excluded.updated_at;

