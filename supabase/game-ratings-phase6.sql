create or replace function public.set_game_rating(
  p_game_id uuid,
  p_rating smallint default null
)
returns table (
  average_rating numeric,
  rating_count bigint,
  user_rating smallint,
  monthly_rating_count bigint
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_actor uuid := auth.uid();
  v_month date := date_trunc('month', now() at time zone 'utc')::date;
  v_recent_actions integer;
begin
  if v_actor is null then
    raise exception 'Authentication required';
  end if;

  if p_rating is not null and (p_rating < 1 or p_rating > 5) then
    raise exception 'Rating must be a whole number from 1 to 5';
  end if;

  if not exists (
    select 1
    from public.games
    where games.id = p_game_id
      and games.status = 'published'
  ) then
    raise exception 'This game is not available for rating';
  end if;

  select count(*)::integer
  into v_recent_actions
  from public.game_rating_events
  where user_id = v_actor
    and created_at >= now() - interval '1 hour';

  if v_recent_actions >= 30 then
    raise exception 'Too many rating changes. Please try again later.';
  end if;

  if p_rating is null then
    delete from public.game_ratings
    where game_id = p_game_id
      and user_id = v_actor;

    delete from public.game_rating_monthly
    where game_id = p_game_id
      and user_id = v_actor
      and rating_month = v_month;

    insert into public.game_rating_events (
      game_id,
      user_id,
      action,
      rating
    ) values (
      p_game_id,
      v_actor,
      'remove',
      null
    );
  else
    insert into public.game_ratings (
      game_id,
      user_id,
      rating,
      created_at,
      updated_at
    ) values (
      p_game_id,
      v_actor,
      p_rating,
      now(),
      now()
    )
    on conflict (game_id, user_id)
    do update set
      rating = excluded.rating,
      updated_at = now();

    insert into public.game_rating_monthly (
      game_id,
      user_id,
      rating_month,
      rating,
      created_at,
      updated_at
    ) values (
      p_game_id,
      v_actor,
      v_month,
      p_rating,
      now(),
      now()
    )
    on conflict (game_id, user_id, rating_month)
    do update set
      rating = excluded.rating,
      updated_at = now();

    insert into public.game_rating_events (
      game_id,
      user_id,
      action,
      rating
    ) values (
      p_game_id,
      v_actor,
      'set',
      p_rating
    );
  end if;

  return query
  select
    round(avg(game_ratings.rating)::numeric, 2),
    count(*)::bigint,
    max(
      case
        when game_ratings.user_id = v_actor then game_ratings.rating
        else null
      end
    )::smallint,
    (
      select count(*)::bigint
      from public.game_rating_monthly
      where game_rating_monthly.game_id = p_game_id
        and game_rating_monthly.rating_month = v_month
    )
  from public.game_ratings
  where game_ratings.game_id = p_game_id;
end;
$$;

create or replace function public.get_game_rating_summary(
  p_game_id uuid
)
returns table (
  average_rating numeric,
  rating_count bigint
)
language sql
stable
security definer
set search_path = public
as $$
  select
    round(avg(game_ratings.rating)::numeric, 2) as average_rating,
    count(*)::bigint as rating_count
  from public.game_ratings
  where game_ratings.game_id = p_game_id;
$$;

create or replace function public.get_game_rating_summaries()
returns table (
  game_id uuid,
  average_rating numeric,
  rating_count bigint
)
language sql
stable
security definer
set search_path = public
as $$
  select
    game_ratings.game_id,
    round(avg(game_ratings.rating)::numeric, 2) as average_rating,
    count(*)::bigint as rating_count
  from public.game_ratings
  join public.games
    on games.id = game_ratings.game_id
  where games.status = 'published'
  group by game_ratings.game_id;
$$;

create or replace function public.get_monthly_top_games(
  p_month_start date default date_trunc('month', now() at time zone 'utc')::date,
  p_limit integer default 5,
  p_min_votes integer default 3
)
returns table (
  game_id uuid,
  name text,
  slug text,
  genre text,
  atlas_score integer,
  cover_image_url text,
  average_rating numeric,
  rating_count bigint,
  weighted_score numeric
)
language sql
stable
security definer
set search_path = public
as $$
  with monthly_scores as (
    select
      game_rating_monthly.game_id,
      avg(game_rating_monthly.rating)::numeric as average_rating,
      count(*)::bigint as rating_count,
      (
        sum(game_rating_monthly.rating)::numeric + (3.5 * 5)
      ) / (
        count(*)::numeric + 5
      ) as weighted_score
    from public.game_rating_monthly
    where game_rating_monthly.rating_month = date_trunc('month', p_month_start::timestamp)::date
    group by game_rating_monthly.game_id
    having count(*) >= greatest(p_min_votes, 1)
  )
  select
    games.id as game_id,
    games.name::text,
    games.slug::text,
    games.genre::text,
    games.atlas_score::integer,
    games.cover_image_url::text,
    round(monthly_scores.average_rating, 2) as average_rating,
    monthly_scores.rating_count,
    round(monthly_scores.weighted_score, 4) as weighted_score
  from monthly_scores
  join public.games
    on games.id = monthly_scores.game_id
  where games.status = 'published'
  order by
    monthly_scores.weighted_score desc,
    monthly_scores.rating_count desc,
    games.atlas_score desc nulls last,
    games.name asc
  limit least(greatest(p_limit, 1), 20);
$$;

